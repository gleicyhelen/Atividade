package atividade.exemplo.gleicy.atividade;

public class ConfigurarCampo {

    private int id;
    private String hint;
    private String tag;


    public  ConfigurarCampo (){}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHint() {
        return hint;
    }

    public void setHint(String hint) {
        this.hint = hint;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }
}
